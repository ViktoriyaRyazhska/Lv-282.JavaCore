package float_numbers;

public class float_numbers {
	
	private float numbers;

	public float_numbers() {
		
	}

	public float getNumbers() {
		return numbers;
	}

	public void setNumbers(float numbers) {
		this.numbers = numbers;
	}

	@Override
	public String toString() {
		return "float_numbers [numbers=" + numbers + "]";
	}


}
